# Создаем DataFrame
df = pd.DataFrame(data, columns=["N", "Код", "Должность", "ФОТ", "На руках", "Прибавка на руках", "Годовой доход"])

# Форматируем числа как рубли с пробелами и символом ₽
def format_rub(x):
    if isinstance(x, (int, float)):
        return f"{x:,.0f} ₽".replace(",", " ")
    return x  # Если значение уже отформатировано или некорректно

for col in ["ФОТ", "На руках", "Прибавка на руках", "Годовой доход"]:
    df[col] = df[col].apply(format_rub)

# --- Цветовая группировка ---
def get_group(row):
    pos = row["Код"]
    if pos in ["ГД", "ИГД", "ТД"]:
        return "1 Руководство"
    elif pos in ["ГИП", "ГАП", "ГА"]:
        return "2 Руководители проектов"
    elif pos in ["ГИПр", "ВИП", "ВИК", "ГК", "ИК 1к", "ИП 1к", "ИП 2к", "ИК 2к"]:
        return "3 Конструкторский отдел"
    elif pos in ["РАО", "ВА", "ВИ", "А 1", "А 2", "ПА"]:
        return "4 Архитектурный отдел"
    elif pos in ["РИО", "ВИП 1", "ИП 1", "ВИП 2"]:
        return "5 Инженерный отдел"      
    else:
        return "Другое"

