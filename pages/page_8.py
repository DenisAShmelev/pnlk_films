# Создаем DataFrame
df = pd.DataFrame(data, columns=["Код", "Должность", "Оклад", "Премия", "Надбавка", "Годовой доход"])

# Форматируем числа как рубли с пробелами и символом ₽
def format_rub(x):
    return f"{x:,.0f} ₽".replace(",", " ")

for col in ["Оклад", "Премия", "Надбавка", "Годовой доход"]:
    df[col] = df[col].apply(format_rub)

# --- Цветовая группировка (как в вашем изображении) ---
# Определим группы по первому символу или ключевым словам:
def get_group(row):
    pos = row["Код"]
    if pos in ["ГД", "ИГД", "ТД"]:
        return "Руководство"
    elif pos in ["ГИП", "ГАП", "ГА", "ГИПр"]:
        return "Главные специалисты"
    elif pos in ["ВИП", "ВИК", "ГК", "ИК 1", "ИП 1", "ИП 2", "ИК 2", "ВИП 1", "ВИП 2"]:
        return "Инженеры"
    elif pos in ["РО", "ВА", "ВИ", "А 1", "А 2", "ПА"]:
        return "Архитекторы и отделы"
    else:
        return "Другое"

df["Группа"] = df.apply(get_group, axis=1)

# Сортируем по группам и коду
df = df.sort_values(["Группа", "Код"]).reset_index(drop=True)

